    private void btnSalvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvar1ActionPerformed
        // TODO add your handling code here:
               String nomeDoenca = txtDoenca.getText().trim().toLowerCase().replace(" ", "_");
        List<String> sintomas = listaSintomas.getSelectedValuesList();

        if(sintomas.size() == 0)
            JOptionPane.showMessageDialog( null, "Não selecionaste nenhum sintoma");
        else
        {
            Boolean existe = false;
            for(String nome : RespostaDAO.getNomesTodasPatologias())
            {    
                if(nome.equalsIgnoreCase(nomeDoenca))
                    existe = true;
            }

            if(existe)       
                JOptionPane.showMessageDialog( null, "A patalogia "+nomeDoenca+" já existe na base de conhecimento ");
            else
            {
                 RespostaDAO.inserirNovaregra(sintomas, nomeDoenca);
                JOptionPane.showMessageDialog( null, "Salvo com sucesso");
            }
        }
    }//GEN-LAST:event_btnSalvar1ActionPerformed

