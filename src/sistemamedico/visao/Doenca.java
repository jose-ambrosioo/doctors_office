    private ListModel<String> getSintomas()
    {
        DefaultListModel<String> sintomas = new DefaultListModel<>();
        
        for (Pergunta p : PerguntaDAO.getPerguntas())
            sintomas.addElement(p.getValor());
        
        return sintomas;
    }
